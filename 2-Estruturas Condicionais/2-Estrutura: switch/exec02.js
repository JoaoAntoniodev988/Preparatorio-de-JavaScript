const opcaoEscolhida = 2;

switch (opcaoEscolhida) {
    case 1:
        console.log("Escolher: Hambúrquer com Batatas");
        break;
    case 2:
        console.log("Escolheu: Pizza de Pepperoni");
        break;
    case 3:
        console.log("Escolheu: Salada Saudável.");
        break;
    default:
        console.log("Opção inválida! Escolha de 1 a 3.");
}